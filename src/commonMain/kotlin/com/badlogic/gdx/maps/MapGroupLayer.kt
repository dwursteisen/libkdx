/**
 * Map layer containing a set of MapLayers, objects and properties
 */
class MapGroupLayer : MapLayer() {

    private val layers: MapLayers? = MapLayers()

    /**
     * @return the [MapLayers] owned by this group
     */
    fun getLayers(): MapLayers? {
        return layers
    }

    override fun invalidateRenderOffset() {
        super.invalidateRenderOffset()
        for (i in 0 until layers!!.size()) {
            val child: MapLayer = layers!!.get(i)
            child!!.invalidateRenderOffset()
        }
    }
}
